#include <cstdio>
#include <cstdlib>
#include <cstring>

#include <map>
#include <algorithm>

typedef const int cint;
typedef long long ll;
typedef unsigned long long ull;

inline bool isdigit( const char cur ) { return cur >= '0' && cur <= '9'; }/*{{{*/
template <class T> 
T Max( T a, T b ) { return a > b? a: b; }
template <class T> 
T Min( T a, T b ) { return a < b? a: b; }
template <class T> 
void chk_Max( T &a, T b ) { if( b > a ) a = b; }
template <class T> 
void chk_Min( T &a, T b ) { if( b < a ) a = b; }
template <typename T>
T read() { 
	T sum = 0, fl = 1; 
	char ch = getchar();
	for (; isdigit(ch) == 0; ch = getchar())
		if (ch == '-') fl = -1;
	for (; isdigit(ch); ch = getchar()) sum = sum * 10 + ch - '0';
	return sum * fl;
}
template <class T> 
T pow( T a, int p ) {
	T res = 1;
	while( p ) {
		if( p & 1 ) 
			res = res * a;
		a = a * a;
		p >>= 1;
	}
	return res;
}/*}}}*/

int main() {
	std::map<int, int> mp;
	int q = read<int>();
	while( q -- ) {
		int op = read<int>();
		if( op == 1 ) {
			cint x = read<int>();
			mp[x] ++;
		}
		if( op == 2 ) {
			cint x = read<int>();
			cint y = read<int>();
			cint k = Min( mp[x], y );;
			mp[x] -= k;
			if( mp[x] == 0 ) 
				mp.erase(x);
		}
		if( op == 3 ) {
			printf( "%d\n", mp.rbegin() -> first - mp.begin() -> first );
		}
	}
}
