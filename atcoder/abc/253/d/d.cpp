#include <cstdio>
#include <cstdlib>
#include <cstring>

#include <algorithm>

typedef const int cint;
typedef long long ll;
typedef unsigned long long ull;

inline bool isdigit( const char cur ) { return cur >= '0' && cur <= '9'; }/*{{{*/
template <class T> 
T Max( T a, T b ) { return a > b? a: b; }
template <class T> 
T Min( T a, T b ) { return a < b? a: b; }
template <class T> 
void chk_Max( T &a, T b ) { if( b > a ) a = b; }
template <class T> 
void chk_Min( T &a, T b ) { if( b < a ) a = b; }
template <typename T>
T read() { 
	T sum = 0, fl = 1; 
	char ch = getchar();
	for (; isdigit(ch) == 0; ch = getchar())
		if (ch == '-') fl = -1;
	for (; isdigit(ch); ch = getchar()) sum = sum * 10 + ch - '0';
	return sum * fl;
}
template <class T> 
T pow( T a, int p ) {
	T res = 1;
	while( p ) {
		if( p & 1 ) 
			res = res * a;
		a = a * a;
		p >>= 1;
	}
	return res;
}/*}}}*/

ll gcd( ll a, ll b ) { return b? gcd( b, a % b ): a; }

ll calc( const ll n, const ll a ) {
	const ll k = n / a;
	return k * ( 1 + k ) / 2LL * a;
}

int main() {
	const ll n = read<int>();
	const ll a = read<int>();
	const ll b = read<int>();

	const ll lcm = a * b / gcd( a, b );

	ll res = calc( n, a ) + calc( n, b ) - calc( n, lcm );
	res = calc( n, 1 ) - res;
	printf( "%lld\n", res );
}
